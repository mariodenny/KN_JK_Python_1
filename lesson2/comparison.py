"""
Comparison Operators

> : greater than (lebih dari)
< : less than (kurang dari)
>= : grater than equal (lebih dari sama dengan)
<= : less than equal (kurang dari sama dengan)
!= : not equal (tidak sama dengan)
== : equals to (sama dengan)

"""

my_first_number = 9
my_second_number = 9
print(my_first_number > my_second_number)
print(my_first_number < my_second_number)
print(my_first_number >= my_second_number)
print(my_first_number <= my_second_number)
print(my_first_number == my_second_number)
print(my_first_number != my_second_number)

