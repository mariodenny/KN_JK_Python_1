"""
Arithmetic operator

* = multiply
+ = add
- = deduction
/ = subtraction
% = modulus (sisa bagi)
** = exponential (perpangkatan)
"""

my_number = 3
my_second_number = 4

print(my_number+my_second_number)
print(my_number-my_second_number)
print(my_number*my_second_number)
print(my_number/my_second_number)
print(my_number**my_second_number)
print(my_number%my_second_number)


